def formatar_descricao_cod_trib_nac(descricao: str, codigo: str, max_chars: int = 72) -> str:
    
    import textwrap

    largura_max = 35

    nova_descricao = f"{codigo} - {descricao}"
    nova_descricao = nova_descricao.strip()[:72] 

    format_code = codigo[:2] + '.' + codigo[2:4] + '.' + codigo[4:6]
    if len(descricao) > 72:
        nova_descricao = format_code + ' - ' + nova_descricao + '...'
    else:
        nova_descricao = format_code + ' - ' + nova_descricao
    linhas = textwrap.wrap(nova_descricao, width=largura_max, break_long_words=False)
    texto_formatado = textwrap.fill(nova_descricao, width=largura_max)

    return texto_formatado

def de_para_codigo_trib_nac(codigo: str, formatar: bool = True, max_chars: int = 72) -> str:
    """
    Retorna a descrição do código tributário nacional.
    
    Args:
        codigo: Código tributário nacional (ex: "010701")
        formatar: Se True, formata a descrição com quebras de linha (padrão: True)
        max_chars: Número máximo de caracteres por linha (padrão: 72)
    
    Returns:
        Descrição do código formatada ou o próprio código se não encontrado
    """
    if not codigo:
        return "-"
    
    codigo = str(codigo).strip()
    descricao = CODIGO_TRIB_NAC.get(codigo, codigo)
    
    if descricao == codigo:
        return codigo
    
    if formatar:
        return formatar_descricao_cod_trib_nac(descricao, codigo, max_chars)
    
    return descricao


